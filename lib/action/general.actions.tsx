export async function getSneakerById(id: string): Promise<Sneaker | null> {
  await new Promise(resolve => setTimeout(resolve, 100));
  return mockDatabase.find(s => s.id === id) || null;
}

export async function searchSneakers(query: string, limit: number = 20): Promise<Sneaker[]> {
  await new Promise(resolve => setTimeout(resolve, 100));
  const lowerQuery = query.toLowerCase();
  
  return mockDatabase
    .filter(sneaker => 
      sneaker.name.toLowerCase().includes(lowerQuery) ||
      sneaker.brand.toLowerCase().includes(lowerQuery) ||
      sneaker.modelName.toLowerCase().includes(lowerQuery) ||
      sneaker.colorway.toLowerCase().includes(lowerQuery)
    )
    .slice(0, limit);
}

export async function getSneakersByBrand(brand: string, limit: number = 20): Promise<Sneaker[]> {
  await new Promise(resolve => setTimeout(resolve, 100));
  
  return mockDatabase
    .filter(sneaker => sneaker.brand.toLowerCase() === brand.toLowerCase())
    .slice(0, limit);
}

export async function getTrendingSneakers(limit: number = 10): Promise<Sneaker[]> {
  await new Promise(resolve => setTimeout(resolve, 100));
  
  return [...mockDatabase]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

